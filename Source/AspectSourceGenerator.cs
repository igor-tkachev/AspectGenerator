		}

		static void GenerateSource(
			SourceProductionContext                         spc,
			Compilation                                     compilation,
			GeneratorExecutionOptions                       options,
			ImmutableArray<AnalyzedInvocation>              aspectedMethods)
		{
			var interceptorsNamespace = GetInterceptorsNamespace(options);

			// Generate source. One file for all the interceptors.
			// Interceptors.g.cs
			//
			var sb = new StringBuilder(
				$$"""
				// <auto-generated/>
				#pragma warning disable
				#nullable enable

				using System;

				using SR  = System.Reflection;
				using SLE = System.Linq.Expressions;
				using SCG = System.Collections.Generic;

				namespace System.Runtime.CompilerServices
				{
					[AttributeUsage(AttributeTargets.Method, AllowMultiple = true)]
					file sealed class InterceptsLocationAttribute(int version, string data) : Attribute
					{
					}
				}

				namespace {{interceptorsNamespace}}
				{
					using AspectGenerator = AspectGenerator;

					file static class AspectGeneratorInterceptors
					{
						static SR.MethodInfo GetMethodInfo(SLE.Expression expr)
						{
							return expr switch
							{
								SLE.MethodCallExpression mc => mc.Method,
								_                           => throw new InvalidOperationException()
							};
						}

						static SR.MethodInfo MethodOf<T>(SLE.Expression<Func<T>> func) => GetMethodInfo(func.Body);
						static SR.MethodInfo MethodOf   (SLE.Expression<Action>  func) => GetMethodInfo(func.Body);


				""");

			var nameCounter = 0;
			var nameSet     = new HashSet<string>();

			string GetInterceptorName(string methodName)
			{
				return nameSet.Add(methodName) ? methodName : GetInterceptorName($"{methodName}_{++nameCounter}");
			}

			foreach (var m in aspectedMethods.GroupBy(m => m.Method, SymbolEqualityComparer.Default).OrderBy(m => m.Key!.Name))
			{
				var method          = (IMethodSymbol)m.Key!;
				var interceptorName = GetInterceptorName($"{method.Name}_Interceptor");
				var methods         = m.ToList();
				var attributes      = methods[0].Attributes;

				if (attributes.Any(a => a.AttributeData?.NamedArguments.Any(na => na.Key == "Order") is true))
				{
					attributes =
						(
							from a in attributes
							let o = a.AttributeData?.NamedArguments.Select(na => (KeyValuePair<string,TypedConstant>?)na).FirstOrDefault(na => na!.Value.Key == "Order")
							let n = o is null ? int.MaxValue : o.Value.Value.Value switch
							{
								string s => int.TryParse(s, out var n) ? n : null,
								int    n => (int?)n,
								_        => null
							}
							orderby n
							select a
						)
						.ToList();
				}

				foreach (var p in method.Parameters)
				{
					switch (p.RefKind)
					{
						case RefKind.Ref :
						case RefKind.Out :
						case RefKind.In  :
							sb.AppendLine(
								$$"""
										static {{p.Type, -30}} {{interceptorName}}_Arg_{{p.Name, -13}} = default({{p.Type}});
								""");
							break;
					}
				}

				sb.AppendLine(
					$$"""
							static SR. MemberInfo                 {{interceptorName}}_MemberInfo        = MethodOf{{GetMethodOf(method, interceptorName)}};
					""");

				for (var i = 0; i < attributes.Count; i++)
				{
					var attr = attributes[i];

					sb
						.AppendLine(
							$$"""
									static SCG.Dictionary<string,object?> {{interceptorName}}_AspectArguments_{{i}} = new()
									{
							""")
						;

					if (attr.AttributeData is not null)
						foreach (var arg in attr.AttributeData.NamedArguments)
						{
							string value;

							if (arg.Value.Type is IArrayTypeSymbol arrayType)
							{
								value = arg.Value.Values.Length switch
								{
									0 => $"new {arrayType.ElementType}[0]",
									_ => $"new {arg.Value.Type} {{ {arg.Value.Values.Select(v => PrintValue(v.Value, arrayType.ElementType)).Aggregate((v1, v2) => $"{v1}, {v2}")} }}"
								};
							}
							else
								value = PrintValue(arg.Value.Value, arg.Value.Type);

							sb
								.AppendLine(
									$$"""
												["{{arg.Key}}"] = {{value}},
									""")
								;

						}

					sb
						.AppendLine(
							"""
									};
							""")
						;
				}

				sb.AppendLine(
					$$"""
							//
							/// <summary>
							/// Intercepts {{method}}.
							/// </summary>
							//
					""");

				foreach (var analyzedInvocation in methods)
				{
					var inv = analyzedInvocation.Inv;

					sb.AppendLine(
						$"""
								// Intercepts {inv}.
						""");

					void AppendInterceptsLocation(InvocationExpressionSyntax invocation)
					{
						var semantic = compilation.GetSemanticModel(invocation.SyntaxTree);

#pragma warning disable RSEXPERIMENTAL002
						var location = semantic.GetInterceptableLocation(invocation, spc.CancellationToken);
#pragma warning restore RSEXPERIMENTAL002

						if (location is null)
						{
							var descriptor = new DiagnosticDescriptor(
								DiagnosticID.CannotIntercept,
								"AspectGenerator",
								$"Invocation '{invocation}' cannot be intercepted by the compiler.",
								"AspectGenerator",
								DiagnosticSeverity.Warning,
								true);

							spc.ReportDiagnostic(
								Diagnostic.Create(descriptor, invocation.GetLocation()));
							return;
						}

						sb.Append("\t\t").AppendLine(location.GetInterceptsLocationAttributeSyntax());
					}

					switch (inv.Expression)
					{
						case MemberAccessExpressionSyntax : AppendInterceptsLocation(inv); break;
						case IdentifierNameSyntax         : AppendInterceptsLocation(inv); break;
						default :
							sb.AppendLine(
								$"""
										#error Unknown expression type {inv.Expression.GetType()} for : {inv.Expression}
								""");
							break;
					}

					sb.AppendLine(
						"""
								//
						""");
				}

				sb.AppendLine("\t\t[System.Runtime.CompilerServices.CompilerGenerated]");

				if (options.DebuggerStepThrough)
					sb.AppendLine("\t\t[System.Diagnostics.DebuggerStepThrough]");

				sb.Append("\t\tpublic static ");

				var methodModifierPosition = sb.Length;

				sb
					.AppendLine(
						$$"""
						{{PrintType(method.ReturnType)}} {{interceptorName}}({{PrintParameters(method)}})
								{
						""")
					;

				GenerateMethodBody(spc, sb, methods[0].Inv, method, interceptorName, attributes, methodModifierPosition);

				sb
					.AppendLine(
						"""
								}
						""")
					.AppendLine();
			}

			TrimEnd(sb);

			sb
				.AppendLine(
					"""
						}
					}
					""");

			spc.AddSource("Interceptors.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
		}

		static void ValidateAspectHooksForMethod(
			List<DiagnosticInfo>    diagnostics,
			Compilation             compilation,
			InvocationExpressionSyntax invocationExpression,
			IMethodSymbol           targetMethod,
			List<AttributeInfo>     attributes,
			HashSet<string>         reportedDiagnostics)
		{
			var targetIsAsync = IsSupportedAsyncReturnType(targetMethod.ReturnType, out var asyncResultType);

			for (var idx = 0; idx < attributes.Count; idx++)
			{
				var attribute       = attributes[idx];
				var aspectArguments = GetAspectArguments(attribute).ToDictionary(static a => a.Key, static a => a.Value);
				var useInterceptData = aspectArguments.TryGetValue("UseInterceptData", out var useData) && useData is true;

				if (idx < attributes.Count - 1 &&
					aspectArguments.TryGetValue("OnCall", out var onCallValue) &&
					onCallValue is string onCall &&
					!string.IsNullOrWhiteSpace(onCall))
				{
					if (attribute.AttributeData?.ApplicationSyntaxReference is {} asr)
					{
						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							DiagnosticID.OnCallNotLastAspect,
							$"Aspect '{attribute.AttributeClass}' specifies 'OnCall' interceptor and has to be the last one in aspect list.",
							Location.Create(asr.SyntaxTree, asr.Span));
					}
					else
					{
						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							DiagnosticID.OnCallNotLastMethod,
							$"Method '{targetMethod}' is decorated with an aspect that specifies 'OnCall' interceptor. This aspect has to be the last one in aspect list.",
							Location.Create(invocationExpression.SyntaxTree, invocationExpression.Span));
					}
				}

				foreach (var hook in GetHookContracts())
				{
					if (!aspectArguments.TryGetValue(hook.Name, out var hookNameValue) ||
						hookNameValue is not string hookName ||
						string.IsNullOrWhiteSpace(hookName))
						continue;

					var location = GetHookLocation(attribute, hook.Name, invocationExpression);

					if (hook.IsAsync && !targetIsAsync && !HasSyncFallback(aspectArguments, hook.Name))
					{
						var asyncVoidMessage = targetMethod is { IsAsync: true, ReturnsVoid: true }
							? " Async void target methods are not supported."
							: "";

						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							DiagnosticID.AsyncHookRequiresTask,
							$"Async hook '{hook.Name}' requires target method '{targetMethod.ToDisplayString()}' to return Task, Task<T>, ValueTask, or ValueTask<T>, or a synchronous fallback hook to be specified.{asyncVoidMessage}",
							location);
					}

					var methods       = attribute.AttributeClass.GetMembers(hookName).OfType<IMethodSymbol>().ToArray();
					var staticMethods = methods.Where(static m => m.IsStatic).ToArray();

					if (methods.Length == 0)
					{
						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							DiagnosticID.HookMethodNotFound,
							$"Aspect hook method '{hookName}' was not found on aspect type '{attribute.AttributeClass.ToDisplayString()}'.",
							location);
						continue;
					}

					if (staticMethods.Length == 0)
					{
						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							DiagnosticID.HookMethodNotStatic,
							$"Aspect hook method '{hookName}' on aspect type '{attribute.AttributeClass.ToDisplayString()}' must be static.",
							location);
						continue;
					}

					if (hook.Name == "OnCall")
					{
						if (!staticMethods.Any(m => IsValidOnCallHook(compilation, m, targetMethod, targetIsAsync, asyncResultType)))
						{
							ReportDiagnostic(
								diagnostics,
								reportedDiagnostics,
								DiagnosticID.OnCallHookMismatch,
								$"OnCall hook '{hookName}' on aspect type '{attribute.AttributeClass.ToDisplayString()}' must match target method '{targetMethod.ToDisplayString()}'.",
								location);
						}

						continue;
					}

					var methodsWithValidParameters = staticMethods
						.Where(m => IsValidLifecycleHookParameter(m, targetMethod, targetIsAsync, asyncResultType, useInterceptData))
						.ToArray();

					if (methodsWithValidParameters.Length == 0)
					{
						var diagnosticId = useInterceptData ? DiagnosticID.HookRequiresInterceptData : DiagnosticID.HookInvalidParameters;

						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							diagnosticId,
							diagnosticId == DiagnosticID.HookRequiresInterceptData
								? $"UseInterceptData=true requires hook method '{hookName}' to accept ref InterceptData<T> for '{hook.Name}'. Candidates: {string.Join("; ", staticMethods.Select(static m => m.ToDisplayString(SymbolDisplayFormat.CSharpErrorMessageFormat)))}."
								: $"Hook method '{hookName}' on aspect type '{attribute.AttributeClass.ToDisplayString()}' has an invalid parameter list for '{hook.Name}'. Candidates: {string.Join("; ", staticMethods.Select(static m => m.ToDisplayString(SymbolDisplayFormat.CSharpErrorMessageFormat)))}.",
							location);
						continue;
					}

					if (!methodsWithValidParameters.Any(m => IsValidLifecycleHookReturnType(compilation, m, hook, targetMethod, targetIsAsync, asyncResultType, useInterceptData)))
					{
						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							DiagnosticID.HookInvalidReturnType,
							$"Hook method '{hookName}' on aspect type '{attribute.AttributeClass.ToDisplayString()}' has an invalid return type for '{hook.Name}'.",
							location);
					}
				}
			}

			static bool HasSyncFallback(Dictionary<string,object?> aspectArguments, string asyncHookName)
			{
				var syncHookName = asyncHookName.EndsWith("Async", StringComparison.Ordinal)
					? asyncHookName[..^5]
					: asyncHookName;

				return syncHookName != asyncHookName &&
					aspectArguments.TryGetValue(syncHookName, out var value) &&
					value is string s &&
					!string.IsNullOrWhiteSpace(s);
			}
		}

		readonly record struct HookContract(string Name, bool IsAsync);

		static IEnumerable<HookContract> GetHookContracts()
		{
			yield return new("OnInit",            false);
			yield return new("OnUsing",           false);
			yield return new("OnUsingAsync",      true);
			yield return new("OnBeforeCall",      false);
			yield return new("OnBeforeCallAsync", true);
			yield return new("OnCall",            false);
			yield return new("OnAfterCall",       false);
			yield return new("OnAfterCallAsync",  true);
			yield return new("OnCatch",           false);
			yield return new("OnCatchAsync",      true);
			yield return new("OnFinally",         false);
			yield return new("OnFinallyAsync",    true);
		}

		static Location GetHookLocation(AttributeInfo attribute, string hookName, InvocationExpressionSyntax invocationExpression)
		{
			if (attribute is { AspectAttributeSyntax: {} syntax, AspectSemanticModel: not null })
			{
				foreach (var arg in syntax.ArgumentList?.Arguments ?? default)
					if (arg.NameEquals?.Name.Identifier.ValueText == hookName)
						return arg.Expression.GetLocation();
			}

			if (attribute.AspectAttributeData?.ApplicationSyntaxReference is {} aspectSyntaxReference)
				return Location.Create(aspectSyntaxReference.SyntaxTree, aspectSyntaxReference.Span);

			if (attribute.AttributeData?.ApplicationSyntaxReference is {} attributeSyntaxReference)
				return Location.Create(attributeSyntaxReference.SyntaxTree, attributeSyntaxReference.Span);

			return invocationExpression.GetLocation();
		}

		static bool IsValidLifecycleHookParameter(
			IMethodSymbol hookMethod,
			IMethodSymbol targetMethod,
			bool          targetIsAsync,
			ITypeSymbol?  asyncResultType,
			bool          useInterceptData)
		{
			if (hookMethod.Parameters.Length != 1)
				return false;

			var parameter = hookMethod.Parameters[0];

			if (useInterceptData)
			{
				if (parameter.RefKind != RefKind.Ref)
					return false;

				return IsInterceptDataType(parameter.Type, hookMethod, GetHookReturnValueType(targetMethod, targetIsAsync, asyncResultType));
			}

			return parameter.RefKind == RefKind.None &&
				(parameter.Type.TypeKind == TypeKind.Dynamic ||
				IsInterceptInfoType(parameter.Type, hookMethod, GetHookReturnValueType(targetMethod, targetIsAsync, asyncResultType), allowNonGeneric: true));
		}

		static bool IsValidLifecycleHookReturnType(
			Compilation   compilation,
			IMethodSymbol hookMethod,
			HookContract  hook,
			IMethodSymbol targetMethod,
			bool          targetIsAsync,
			ITypeSymbol?  asyncResultType,
			bool          useInterceptData)
		{
			if (hook.Name == "OnInit")
				return useInterceptData
					? IsInterceptDataType(hookMethod.ReturnType, hookMethod, GetHookReturnValueType(targetMethod, targetIsAsync, asyncResultType))
					: IsInterceptInfoType(hookMethod.ReturnType, hookMethod, GetHookReturnValueType(targetMethod, targetIsAsync, asyncResultType), allowNonGeneric: false);

			if (hook.Name == "OnUsing")
				return ImplementsType(hookMethod.ReturnType, "System.IDisposable");

			if (hook.Name == "OnUsingAsync")
				return ImplementsType(hookMethod.ReturnType, "System.IAsyncDisposable");

			if (hook.IsAsync)
				return IsSupportedAsyncReturnType(hookMethod.ReturnType, out _);

			return hookMethod.ReturnsVoid;
		}

		static bool IsValidOnCallHook(
			Compilation   compilation,
			IMethodSymbol hookMethod,
			IMethodSymbol targetMethod,
			bool          targetIsAsync,
			ITypeSymbol?  asyncResultType)
		{
			var expectedParameters = GetOnCallParameters(targetMethod).ToArray();

			if (hookMethod.Parameters.Length != expectedParameters.Length)
				return false;

			for (var i = 0; i < expectedParameters.Length; i++)
			{
				var actual   = hookMethod.Parameters[i];
				var expected = expectedParameters[i];

				if (actual.RefKind != expected.RefKind)
					return false;

				if (!SymbolEqualityComparer.Default.Equals(actual.Type, expected.Type))
					return false;
			}

			if (targetMethod.ReturnsVoid)
				return hookMethod.ReturnsVoid;

			if (targetIsAsync)
				return IsSupportedAsyncReturnType(hookMethod.ReturnType, out var hookAsyncResultType) &&
					(asyncResultType is null && hookAsyncResultType is null ||
					 asyncResultType is not null &&
					 hookAsyncResultType is not null &&
					 SymbolEqualityComparer.Default.Equals(asyncResultType, hookAsyncResultType));

			return compilation.ClassifyConversion(hookMethod.ReturnType, targetMethod.ReturnType).IsImplicit;
		}

		static IEnumerable<(ITypeSymbol Type, RefKind RefKind)> GetOnCallParameters(IMethodSymbol targetMethod)
		{
			if (targetMethod.IsExtensionMethod || !targetMethod.IsStatic)
			{
				var receiverType = targetMethod.ReceiverType ?? targetMethod.ContainingType;

				if (receiverType is not null)
					yield return (receiverType, RefKind.None);
			}

			foreach (var parameter in targetMethod.Parameters)
				yield return (parameter.Type, parameter.RefKind);
		}

		static (ITypeSymbol? Type, bool IsVoid) GetHookReturnValueType(IMethodSymbol targetMethod, bool targetIsAsync, ITypeSymbol? asyncResultType)
		{
			if (targetMethod.ReturnsVoid || targetIsAsync && asyncResultType is null)
				return (null, true);

			return targetIsAsync ? (asyncResultType, false) : (targetMethod.ReturnType, false);
		}

		static bool IsInterceptInfoType(
			ITypeSymbol                    type,
			IMethodSymbol                  hookMethod,
			(ITypeSymbol? Type, bool IsVoid) expectedValueType,
			bool                           allowNonGeneric)
		{
			if (type is not INamedTypeSymbol namedType)
				return false;

			if (!IsAspectGeneratorType(namedType, "InterceptInfo"))
				return IsUnresolvedCarrierType(type, "InterceptInfo", allowNonGeneric);

			if (namedType.Arity == 0)
				return allowNonGeneric;

			return namedType is { Arity: 1, TypeArguments: [var valueType] } &&
				TypeArgumentMatches(valueType, hookMethod, expectedValueType);
		}

		static bool IsInterceptDataType(
			ITypeSymbol                    type,
			IMethodSymbol                  hookMethod,
			(ITypeSymbol? Type, bool IsVoid) expectedValueType)
		{
			if (type is not INamedTypeSymbol namedType)
				return false;

			if (!IsAspectGeneratorType(namedType, "InterceptData"))
				return IsUnresolvedCarrierType(type, "InterceptData", allowNonGeneric: false);

			return namedType is { Arity: 1, TypeArguments: [var valueType] } &&
				TypeArgumentMatches(valueType, hookMethod, expectedValueType);
		}

		static bool IsUnresolvedCarrierType(ITypeSymbol type, string name, bool allowNonGeneric)
		{
			var displayName = type.ToDisplayString(SymbolDisplayFormat.CSharpErrorMessageFormat);

			return allowNonGeneric && displayName == name ||
				displayName.StartsWith(name + "<", StringComparison.Ordinal);
		}

		static bool TypeArgumentMatches(
			ITypeSymbol                    type,
			IMethodSymbol                  hookMethod,
			(ITypeSymbol? Type, bool IsVoid) expectedValueType)
		{
			if (type is ITypeParameterSymbol typeParameter &&
				SymbolEqualityComparer.Default.Equals(typeParameter.ContainingSymbol, hookMethod))
				return true;

			if (expectedValueType.IsVoid)
				return type.SpecialType == SpecialType.System_Void ||
					type.ToDisplayString(SymbolDisplayFormat.CSharpErrorMessageFormat) == "System.Void" ||
					type is INamedTypeSymbol namedType && IsAspectGeneratorType(namedType, "Void");

			return expectedValueType.Type is not null &&
				SymbolEqualityComparer.Default.Equals(type, expectedValueType.Type);
		}

		static bool IsAspectGeneratorType(INamedTypeSymbol type, string name)
		{
			if (type.Name != name)
				return false;

			var @namespace = type.ContainingNamespace.ToDisplayString();

			return @namespace is "" or "AspectGenerator";
		}

		static bool IsSupportedAsyncReturnType(ITypeSymbol type, out ITypeSymbol? resultType)
		{
			resultType = null;

			if (type is not INamedTypeSymbol namedType ||
				namedType.Name is not ("Task" or "ValueTask") ||
				namedType.ContainingNamespace.ToDisplayString() != "System.Threading.Tasks")
				return false;

			if (namedType is { IsGenericType: true, TypeArguments: [var asyncResultType] })
				resultType = asyncResultType;

			return true;
		}

		static bool ImplementsType(ITypeSymbol type, string metadataName)
		{
			return IsType(type, metadataName) ||
				type.AllInterfaces.Any(i => IsType(i, metadataName));
		}

		static bool IsType(ITypeSymbol type, string metadataName)
		{
			if (type is not INamedTypeSymbol namedType)
				return false;

			return namedType.ContainingNamespace.ToDisplayString() + "." + namedType.Name == metadataName;
		}

		static void GenerateMethodBody(
			SourceProductionContext    spc,
			StringBuilder              sb,
			InvocationExpressionSyntax invocationExpression,
			IMethodSymbol              method,
			string                     interceptorName,
			List<AttributeInfo>        attributes,
			int                        methodModifierPosition)
		{
			var isReturnsAsyncType = IsSupportedAsyncReturnType(method.ReturnType, out var asyncResultType);
			var generateAsync      = isReturnsAsyncType && attributes.Any(d => GetAspectArguments(d).Any(a => a.Key.EndsWith("Async") && a.Value is not null));
			var generateArgs       = attributes.Any(d => GetAspectArguments(d).Any(a => a is { Key: "PassArguments", Value: true }));

			// Generate arguments.
			//
			if (generateArgs)
			{
				sb.Append("\t\t\tvar __args__ = new object?[] { ");

				if (method.IsStatic == false)
					sb.Append("__this__, ");

				foreach (var p in method.Parameters)
					sb.Append(p.Name).Append(p.RefKind == RefKind.Out ? " = default, " : ", ");

				while (sb[^1] is ' ' or ',')
					sb.Length--;

				sb.Append(" };").AppendLine().AppendLine();
			}

			GenerateAttribute(0, "\t\t\t", false);

			void GenerateAttribute(int idx, string indent, bool isPrevData)
			{
				var attr   = attributes[idx].AttributeClass!;

				// Get aspect attribute parameters.
				//
				object? onInit           = null;
				object? onUsing          = null; object? onUsingAsync      = null;
				object? onBeforeCall     = null; object? onBeforeCallAsync = null;
				object? onCall           = null;
				object? onAfterCall      = null; object? onAfterCallAsync  = null;
				object? onCatch          = null; object? onCatchAsync      = null;
				object? onFinally        = null; object? onFinallyAsync    = null;
				var     useInterceptType = false;
				var     passArguments    = false;
				var     useInterceptData = false;
				var     needInfo         = attributes.Count > 1 || generateAsync;

				foreach (var arg in GetAspectArguments(attributes[idx]))
					switch (arg.Key)
					{
						case "OnInit"            : onInit            = arg.Value; needInfo = true; break;
						case "OnUsing"           : onUsing           = arg.Value; needInfo = true; break;
						case "OnUsingAsync"      : onUsingAsync      = arg.Value; needInfo = true; break;
						case "OnBeforeCall"      : onBeforeCall      = arg.Value; needInfo = true; break;
						case "OnBeforeCallAsync" : onBeforeCallAsync = arg.Value; needInfo = true; break;
						case "OnCall"            : onCall            = arg.Value;                  break;
						case "OnAfterCall"       : onAfterCall       = arg.Value; needInfo = true; break;
						case "OnAfterCallAsync"  : onAfterCallAsync  = arg.Value; needInfo = true; break;
						case "OnCatch"           : onCatch           = arg.Value; needInfo = true; break;
						case "OnCatchAsync"      : onCatchAsync      = arg.Value; needInfo = true; break;
						case "OnFinally"         : onFinally         = arg.Value; needInfo = true; break;
						case "OnFinallyAsync"    : onFinallyAsync    = arg.Value; needInfo = true; break;
						case "UseInterceptType"  : useInterceptType  = arg.Value is true; break;
						case "PassArguments"     : passArguments     = arg.Value is true; break;
						case "UseInterceptData"  : useInterceptData  = arg.Value is true; break;
					}

				if (generateAsync)
					sb.Insert(methodModifierPosition, "async ");

				var returnType = generateAsync ? asyncResultType?.ToString() ?? "AspectGenerator.Void" : method.ReturnsVoid ? "AspectGenerator.Void" : $"{method.ReturnType}";

				// Generate AspectCallInfo.
				//
				if (needInfo)
				{
					sb
						.Append(indent).AppendLine($"// {(object?)attributes[idx].AttributeData ?? attributes[idx].AttributeClass}")
						.Append(indent).AppendLine("//")
						//.Append(indent).Append($"var __attr__{idx} = new {attributes[idx]}").Append(attributes[idx].NamedArguments.Length == 0 ? "()" : "").AppendLine(";")
						.Append(indent).AppendLine($"var __info__{idx} = new AspectGenerator.Intercept{(useInterceptData ? "Data" : "Info")}<{returnType}>")
						.Append(indent).AppendLine("{")
						//.Append(indent).AppendLine($"\tReturnValue     = {(idx > 0 ? $"__info__{idx - 1}.ReturnValue" : $"default({(method.ReturnsVoid ? "Void" : $"{method.ReturnType}")})")},")
						.Append(indent).AppendLine($"\tMemberInfo      = {interceptorName}_MemberInfo,")
						.Append(indent).AppendLine($"\tAspectType      = typeof({attr}),")
						.Append(indent).AppendLine($"\tAspectArguments = {interceptorName}_AspectArguments_{idx},")
						;

					if (passArguments)
						sb
							.Append(indent).AppendLine("\tMethodArguments = __args__,")
							;

					if (idx > 0 && !isPrevData)
						sb.Append(indent).AppendLine($"\tPreviousInfo    = __info__{idx - 1}");

					sb
						.Append(indent).AppendLine("};")
						.AppendLine()
						;
				}

				// Generate OnInit.
				//
				if (onInit is not null)
				{
					GenerateInterceptType("", "OnInit")
						.Append(indent).AppendLine($"__info__{idx} = {attr.ContainingNamespace}.{attr.Name}.{onInit}({PassInfo(idx)});")
						.AppendLine()
						;
				}

				// Generate OnUsing.
				//
				if (generateAsync && onUsingAsync is not null || onUsing is not null)
				{
					var isAsync = generateAsync && onUsingAsync != null;
					GenerateInterceptType("", "OnUsing")
						.Append(indent).AppendLine($"{(isAsync ? "await " : "")}using ({attr.ContainingNamespace}.{attr.Name}.{(isAsync ? onUsingAsync : onUsing)}({PassInfo(idx)}))")
						.Append(indent).AppendLine("{")
						;
					indent += '\t';
				}

				// If OnCatch or OnFinally is defined, generate try/catch/finally block.
				//
				if (generateAsync && onCatchAsync   is not null || onCatch   is not null ||
					generateAsync && onFinallyAsync is not null || onFinally is not null)
				{
					sb.Append(indent).AppendLine("try");
					sb.Append(indent).AppendLine("{");
					indent += '\t';
				}

				// Generate OnBeforeCall.
				//
				if (generateAsync && onBeforeCallAsync is not null || onBeforeCall is not null)
				{
					GenerateInterceptType("", "OnBeforeCall");
					GenerateMethodCall(onBeforeCallAsync, onBeforeCall)
						.AppendLine()
						.Append(indent).AppendLine($"if (__info__{idx}.InterceptResult != AspectGenerator.InterceptResult.Return)")
						.Append(indent).AppendLine("{")
						;

					indent += '\t';
				}

				// Generate next attribute...
				//
				if (idx < attributes.Count - 1)
				{
					sb.Append(indent).AppendLine("{");

					GenerateAttribute(idx + 1, indent + '\t', useInterceptData);

					TrimEnd(sb);

					sb
						.Append(indent).AppendLine("}")
						.AppendLine()
						;
				}
				// .. or call the target method.
				//
				else
				{
					sb
						.Append(indent)
						.Append(
							method.ReturnsVoid || isReturnsAsyncType && asyncResultType is null
								? string.Empty
								: $"{(needInfo ? $"__info__{idx}.ReturnValue" : "var __return__")} = {(generateAsync ? "await " : "")}")
						.Append(
							onCall is null
								? method.IsExtensionMethod || method.IsStatic
									? method.OriginalDefinition.ContainingType
									: "__this__"
								: $"{attr.ContainingNamespace}.{attr.Name}")
						.Append('.')
						.Append(onCall ?? method.Name)
						.Append('(')
						;

					if (method.IsExtensionMethod || onCall is not null && method.IsStatic is false)
					{
						sb.Append("__this__");

						if (method.Parameters.Length > 0)
							sb.Append(", ");
					}

					foreach (var p in method.Parameters)
					{
						sb
							.Append(
								p.RefKind switch
								{
									RefKind.Ref => "ref ",
									RefKind.Out => "out ",
									RefKind.In  => "in ",
									_           => ""
								})
							.Append(p.Name)
							.Append(", ");
					}

					if (method.Parameters.Length > 0)
						sb.Length -= 2;

					sb
						.AppendLine(");")
						.AppendLine()
						;
				}

				// Generate OnAfterCall.
				//
				if (generateAsync && onAfterCallAsync is not null || onAfterCall is not null)
				{
					GenerateInterceptType("", "OnAfterCall");
					GenerateMethodCall(onAfterCallAsync, onAfterCall)
						.AppendLine();
				}

				// Generate OnBeforeCall end.
				//
				if (generateAsync && onBeforeCallAsync is not null || onBeforeCall is not null)
				{
					TrimEnd(sb);

					indent = indent[..^1];

					sb
						.Append(indent)
						.AppendLine("}")
						.AppendLine()
						;
				}

				// If OnCatch or OnFinally is defined, generate try/catch/finally block end.
				//
				if (generateAsync && onCatchAsync   is not null || onCatch   is not null ||
					generateAsync && onFinallyAsync is not null || onFinally is not null)
				{
					indent = indent[..^1];

					TrimEnd(sb);

					sb.AppendLine($"{indent}}}");

					// Generate OnCatch.
					//
					sb
						.Append(indent).AppendLine("catch (Exception __ex__)")
						.Append(indent).AppendLine("{")
						.Append(indent).AppendLine($"\t__info__{idx}.Exception{(onCatch is null ? null : "      ")} = __ex__;")
						;

					if (generateAsync && onCatchAsync is not null || onCatch is not null)
					{
						sb.Append(indent).AppendLine($"\t__info__{idx}.InterceptResult = AspectGenerator.InterceptResult.ReThrow;");

						GenerateInterceptType("\t", "OnCatch")
							.AppendLine()
							.Append('\t')
							;

						GenerateMethodCall(onCatchAsync, onCatch)
							.AppendLine()
							.Append(indent).AppendLine($"\tif (__info__{idx}.InterceptResult == AspectGenerator.InterceptResult.ReThrow)")
							.Append(indent).AppendLine("\t\tthrow;")
							;
					}
					else
					{
						sb.Append(indent).AppendLine("\tthrow;");
					}

					sb.Append(indent).AppendLine("}");

					// Generate OnFinally.
					//
					if (generateAsync && onFinallyAsync is not null || onFinally is not null)
					{
						sb
							.Append(indent).AppendLine("finally")
							.Append(indent).AppendLine("{");

						GenerateInterceptType("\t", "OnFinally")
							.Append('\t')
							;

						GenerateMethodCall(onFinallyAsync, onFinally)
							.Append(indent).AppendLine("}")
							;
					}

					sb.AppendLine();
				}

				// Generate OnUsing.
				//
				if (generateAsync && onUsingAsync is not null || onUsing is not null)
				{
					indent = indent[..^1];

					TrimEnd(sb);

					sb
						.Append(indent)
						.AppendLine("}")
						.AppendLine()
						;
				}

				// Generate return.
				//
				if (idx > 0)
					sb.Append(indent).AppendLine($"__info__{idx - 1}.ReturnValue = __info__{idx}.ReturnValue;");
				else if (!(method.ReturnsVoid || isReturnsAsyncType && asyncResultType is null))
					sb.AppendLine($"\t\t\treturn {(needInfo ? $"__info__{idx}.ReturnValue" : "__return__")};");

				TrimEnd(sb);

				string PassInfo(int idx)
				{
					return $"{(useInterceptData ? "ref " : "")}__info__{idx}";
				}

				StringBuilder GenerateMethodCall(object? onAsync, object? call)
				{
					if (generateAsync && onAsync is not null)
						return sb.Append(indent).AppendLine($"await {attr.ContainingNamespace}.{attr.Name}.{onAsync}({PassInfo(idx)});");
					return sb.Append(indent).AppendLine($"{attr.ContainingNamespace}.{attr.Name}.{call}({PassInfo(idx)});");
				}

				StringBuilder GenerateInterceptType(string additionalIndent, string interceptType)
				{
					if (useInterceptType)
					{
						sb
							.Append(additionalIndent)
							.Append(indent)
							.AppendLine($"__info__{idx}.InterceptType = AspectGenerator.InterceptType.{interceptType};");
					}

					return sb;
				}
			}
		}

		static void TrimEnd(StringBuilder sb)
		{
			while (sb[^1] is '\r' or '\n')
				sb.Length--;

			sb.AppendLine();
		}

		static string PrintValue(object? val, ITypeSymbol? type)
		{
			if (val is null)
				return "null";

			if (type?.TypeKind == TypeKind.Enum)
				return $"({type}){PrintValue(val, null)}";

			return val switch
			{
				string           s => SymbolDisplay.FormatLiteral(s, quote: true),
				char             c => SymbolDisplay.FormatLiteral(c, quote: true),
				bool             b => b ? "true" : "false",
				byte             b => b.ToString(CultureInfo.InvariantCulture),
				sbyte            b => b.ToString(CultureInfo.InvariantCulture),
				short            n => n.ToString(CultureInfo.InvariantCulture),
				ushort           n => n.ToString(CultureInfo.InvariantCulture),
				int              n => n.ToString(CultureInfo.InvariantCulture),
				uint             n => n.ToString(CultureInfo.InvariantCulture) + "u",
				long             n => n.ToString(CultureInfo.InvariantCulture) + "L",
				ulong            n => n.ToString(CultureInfo.InvariantCulture) + "uL",
				float            f => PrintSingle(f),
				double           d => PrintDouble(d),
				decimal          d => d.ToString(CultureInfo.InvariantCulture) + "m",
				INamedTypeSymbol s => $"typeof({s.ToDisplayString(SymbolDisplayFormat.FullyQualifiedFormat)})",
				_                  => Convert.ToString(val, CultureInfo.InvariantCulture) ?? "null"
			};

			static string PrintSingle(float value)
			{
				if (float.IsNaN(value))
					return "float.NaN";
				if (float.IsPositiveInfinity(value))
					return "float.PositiveInfinity";
				if (float.IsNegativeInfinity(value))
					return "float.NegativeInfinity";

				return value.ToString("R", CultureInfo.InvariantCulture) + "f";
			}

			static string PrintDouble(double value)
			{
				if (double.IsNaN(value))
					return "double.NaN";
				if (double.IsPositiveInfinity(value))
					return "double.PositiveInfinity";
				if (double.IsNegativeInfinity(value))
					return "double.NegativeInfinity";

				return value.ToString("R", CultureInfo.InvariantCulture) + "d";
			}
		}

		static string PrintType(ITypeSymbol type)
		{
			return $"{type}";
		}

		static string PrintParameters(IMethodSymbol method)
		{
			var parameters = method.Parameters;
			var sb         = new StringBuilder();

			if (method.IsStatic == false)
				sb.Append($"this {method.ReceiverType} __this__, ");

			foreach (var p in parameters)
				sb
					.Append(p)
					.Append(", ")
					;

			if (parameters.Length != 0 || method.IsStatic == false)
				sb.Length -= 2;

			return sb.ToString();
		}

		static string GetMethodOf(IMethodSymbol method, string interceptorName)
		{
			var sb = new StringBuilder();

			sb.Append("(() => ");

			sb
				.Append(method.IsExtensionMethod || method.IsStatic ? method.OriginalDefinition.ContainingType : $"default({method.ReceiverType})")
				.Append('.')
				.Append(method.Name)
				.Append('(')
				;

			if (method.IsExtensionMethod)
				sb.Append($"default({method.ReceiverType}), ");

			foreach (var p in method.Parameters)
				switch (p.RefKind)
				{
					case RefKind.Ref : sb.Append($"ref {interceptorName}_Arg_{p.Name}, "); break;
					case RefKind.In  : sb.Append($"in {interceptorName}_Arg_{p.Name}, ");  break;
					case RefKind.Out : sb.Append($"out {interceptorName}_Arg_{p.Name}, "); break;
					default          : sb.Append($"default({p.Type}), ");                  break;
				}

			while (sb[^1] is ',' or ' ')
				sb.Length--;

			sb.Append("))");

			return sb.ToString();
		}
	}

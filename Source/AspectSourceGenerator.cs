		public void Initialize(IncrementalGeneratorInitializationContext context)
		{
#if DEBUG && TRUE1
			if (!System.Diagnostics.Debugger.IsAttached)
			{
				System.Diagnostics.Debugger.Launch();
			}
#endif

			context.RegisterPostInitializationOutput(ctx => ctx.AddSource(
				"AspectGeneratorOptionsAttribute.g.cs",
				SourceText.From(AspectGeneratorOptionsAttributeText, Encoding.UTF8)));

			var attributes = context.SyntaxProvider
				.CreateSyntaxProvider(
					predicate: static (node, _) => node is ClassDeclarationSyntax { AttributeLists.Count: > 0 } c && c.AttributeLists
						.SelectMany(static l => l.Attributes)
						.Any(static a => IsAspectAttributeName(a.Name.ToString())),
					transform: static (ctx, _) => (ClassDeclarationSyntax)ctx.Node)
				.Collect();

			var invocations = context.SyntaxProvider
				.CreateSyntaxProvider(
					predicate: static (node, _) => node is InvocationExpressionSyntax
					{
						Expression: MemberAccessExpressionSyntax or IdentifierNameSyntax
					},
					transform: static (ctx, _) => (InvocationExpressionSyntax)ctx.Node)
				.Collect();

			var options = context.AnalyzerConfigOptionsProvider.Select((c, _) =>
				new Options
				{
					GenerateApi            = GetBoolProperty(c.GlobalOptions, $"build_property.AspectGenerator{OptionID.GenerateApi}"),
					GenerateInterceptors   = GetBoolProperty(c.GlobalOptions, $"build_property.AspectGenerator{OptionID.GenerateInterceptors}"),
					DesignTimeBuild        = GetBoolProperty(c.GlobalOptions,  "build_property.DesignTimeBuild"),
					PublicApi              = GetBoolProperty(c.GlobalOptions, $"build_property.AspectGenerator{OptionID.PublicApi}"),
					DebuggerStepThrough    = GetBoolProperty(c.GlobalOptions, $"build_property.AspectGenerator{OptionID.DebuggerStepThrough}"),
					InterceptorsNamespace  = c.GlobalOptions.TryGetValue($"build_property.AspectGenerator{OptionID.InterceptorsNamespace}", out var ns) ? ns : null,
					InterceptorsNamespaces = c.GlobalOptions.TryGetValue("build_property.InterceptorsNamespaces", out var namespaces) ? namespaces : null,
				});

			var analysis = context.CompilationProvider
				.Combine(options)
				.Combine(attributes)
				.Combine(invocations)
				.Select(static (data, ct) => Analyze(data, ct));

			context.RegisterSourceOutput(analysis, ReportDiagnostics);
			context.RegisterImplementationSourceOutput(analysis, EmitInterceptors);
		}

		static AnalysisResult Analyze(
			(((Compilation Left, Options Right) Left, ImmutableArray<ClassDeclarationSyntax> Right) Left, ImmutableArray<InvocationExpressionSyntax> Right) data,
			CancellationToken cancellationToken)
		{
			var compilation = data.Left.Left.Left;
			var options     = ResolveOptions(compilation, data.Left.Left.Right);
			var attrs       = data.Left.Right;
			var invocations = data.Right;

			var diagnostics = new List<DiagnosticInfo>();
			var reportedDiagnostics = new HashSet<string>();

			var aspectAttributes = new Dictionary<ISymbol,(AttributeSyntax Syntax,SemanticModel SemanticModel)>(SymbolEqualityComparer.Default);

			foreach (var a in attrs)
			{
				var semanticModel = compilation.GetSemanticModel(a.SyntaxTree);

				if (semanticModel.GetDeclaredSymbol(a, cancellationToken) is not {} symbol)
					continue;

				foreach (var attribute in a.AttributeLists.SelectMany(static l => l.Attributes))
				{
					if (!IsAspectAttributeName(attribute.Name.ToString()))
						continue;

					aspectAttributes[symbol] = (attribute, semanticModel);
					break;
				}
			}

			ValidateAspectHooks(diagnostics, aspectAttributes, reportedDiagnostics);

			var assemblyFilters = BuildAssemblyFilters(compilation, diagnostics, aspectAttributes, reportedDiagnostics);

			var aspectedMethods  = new List<AnalyzedInvocation>();
			var methodDic        = new Dictionary<IMethodSymbol,List<AttributeInfo>>(SymbolEqualityComparer.Default);
			var typeFilterDic    = new Dictionary<INamedTypeSymbol,ImmutableArray<AspectFilterSet>>(SymbolEqualityComparer.Default);

			foreach (var invocation in invocations)
			{
				var semantic = compilation.GetSemanticModel(invocation.SyntaxTree);
				var info     = semantic.GetSymbolInfo(invocation, cancellationToken);

				// Bind only invocation candidates provided by SyntaxProvider.
				//
				if (info.Symbol is IMethodSymbol method)
				{
					if (!methodDic.TryGetValue(method, out var attributes))
					{
						attributes = new();

						var methodAttributes = method.GetAttributes();

						// .. decorated with any Aspect attribute...
						//
						if (methodAttributes.Length > 0)
						{
							foreach (var ma in methodAttributes)
							{
								// .. if attribute is defined in the compiling assembly...
								//
								if (ma.AttributeClass is not null && aspectAttributes.TryGetValue(ma.AttributeClass, out var aspectAttribute))
								{
									attributes.Add(new AttributeInfo(ma, ma.AttributeClass, null, aspectAttribute.Syntax, aspectAttribute.SemanticModel));
								}
								// .. or somewhere else.
								else if (ma.AttributeClass?.GetAttributes().FirstOrDefault(aa => aa is { AttributeClass : { ContainingNamespace.Name : "AspectGenerator", Name : "AspectAttribute" }}) is {} externalAspectAttributeData)
								{
									attributes.Add(new AttributeInfo(ma, ma.AttributeClass!, externalAspectAttributeData, null, null));
								}
							}
						}

						var targetSignature = GetCanonicalMethodFilterSignature(method);

						AddMatchedFilterAttributes(attributes, assemblyFilters, targetSignature);
						AddMatchedFilterAttributes(attributes, GetTypeFilters(compilation, diagnostics, aspectAttributes, typeFilterDic, method.ContainingType, reportedDiagnostics), targetSignature);

						methodDic[method] = attributes.Distinct().ToList();
					}

					if (attributes.Count > 0)
						aspectedMethods.Add(new AnalyzedInvocation(invocation, method, attributes));
				}

				if (cancellationToken.IsCancellationRequested)
					break;
			}

			if (!cancellationToken.IsCancellationRequested)
				foreach (var m in aspectedMethods.GroupBy(static m => m.Method, SymbolEqualityComparer.Default))
					ValidateAspectHooksForMethod(diagnostics, compilation, m.First().Inv, (IMethodSymbol)m.Key!, m.First().Attributes, reportedDiagnostics);

			ReportMissingInterceptorsNamespace(diagnostics, options);

			return new AnalysisResult(compilation, options, aspectedMethods.ToImmutableArray(), diagnostics.ToImmutableArray());
		}

		static void ReportDiagnostics(SourceProductionContext spc, AnalysisResult analysis)
		{
			if (analysis.Options.GenerateApi)
				spc.AddSource("AspectAttribute.g.cs", SourceText.From(GenerateAspectApiText(analysis.Options.PublicApi), Encoding.UTF8));

			foreach (var diagnostic in analysis.Diagnostics)
			{
				spc.ReportDiagnostic(
					Diagnostic.Create(
						diagnostic.Id,
						"AspectGenerator",
						diagnostic.Message,
						diagnostic.IsError ? DiagnosticSeverity.Error : DiagnosticSeverity.Warning,
						diagnostic.IsError ? DiagnosticSeverity.Error : DiagnosticSeverity.Warning,
						true,
						0,
						location: diagnostic.Location));
			}
		}

		static void EmitInterceptors(SourceProductionContext spc, AnalysisResult analysis)
		{
			if (!analysis.Options.GenerateInterceptors ||
				analysis.AspectedMethods.Length == 0 ||
				spc.CancellationToken.IsCancellationRequested)
				return;

			GenerateSource(spc, analysis.Compilation, analysis.Options, analysis.AspectedMethods);
		}

		static bool? GetBoolProperty(AnalyzerConfigOptions options, string name)
		{
			if (!options.TryGetValue(name, out var value))
				return null;

			return bool.TryParse(value, out var result) ? result : null;
		}

		static GeneratorExecutionOptions ResolveOptions(Compilation compilation, Options msBuildOptions)
		{
			var result = new Options
			{
				GenerateApi                   = msBuildOptions.GenerateApi,
				GenerateInterceptors          = msBuildOptions.GenerateInterceptors,
				DesignTimeBuild               = msBuildOptions.DesignTimeBuild,
				PublicApi                     = msBuildOptions.PublicApi,
				DebuggerStepThrough           = msBuildOptions.DebuggerStepThrough,
				InterceptorsNamespace         = msBuildOptions.InterceptorsNamespace,
				InterceptorsNamespaces = msBuildOptions.InterceptorsNamespaces,
			};

			var attr = compilation.Assembly.GetAttributes().FirstOrDefault(a =>
				a.AttributeClass is { ContainingNamespace.Name: "AspectGenerator", Name: "AspectGeneratorOptionsAttribute" });

			if (attr is null)
				return CreateExecutionOptions(result);

			foreach (var arg in attr.NamedArguments)
			{
				switch (arg.Key)
				{
					case OptionID.GenerateApi           when arg.Value.Value is bool   generateApi          : result.GenerateApi           = generateApi;           break;
					case OptionID.GenerateInterceptors  when arg.Value.Value is bool   generateInterceptors : result.GenerateInterceptors  = generateInterceptors;  break;
					case OptionID.PublicApi             when arg.Value.Value is bool   publicApi            : result.PublicApi             = publicApi;             break;
					case OptionID.DebuggerStepThrough   when arg.Value.Value is bool   debuggerStepThrough  : result.DebuggerStepThrough   = debuggerStepThrough;   break;
					case OptionID.InterceptorsNamespace when arg.Value.Value is string interceptorsNamespace: result.InterceptorsNamespace = interceptorsNamespace; break;
				}
			}

			return CreateExecutionOptions(result);

			static GeneratorExecutionOptions CreateExecutionOptions(Options options)
			{
				var generateInterceptors = options.GenerateInterceptors ?? options.DesignTimeBuild is not true;

				return new GeneratorExecutionOptions(
					options.GenerateApi is not false,
					generateInterceptors,
					options.PublicApi is true,
					options.DebuggerStepThrough is true,
					options.InterceptorsNamespace,
					options.InterceptorsNamespaces);
			}
		}

		static string GetInterceptorsNamespace(GeneratorExecutionOptions options)
		{
			return string.IsNullOrWhiteSpace(options.InterceptorsNamespace) ? "AspectGenerator" : options.InterceptorsNamespace!;
		}

		static void ReportMissingInterceptorsNamespace(List<DiagnosticInfo> diagnostics, GeneratorExecutionOptions options)
		{
			var interceptorsNamespace = GetInterceptorsNamespace(options);

			if (string.IsNullOrWhiteSpace(options.InterceptorsNamespaces))
				return;

			var namespaces = options.InterceptorsNamespaces!
				.Split([';'], StringSplitOptions.RemoveEmptyEntries)
				.Select(static n => n.Trim());

			if (namespaces.Contains(interceptorsNamespace, StringComparer.Ordinal))
				return;

			diagnostics.Add(
				new DiagnosticInfo(
				DiagnosticID.NamespaceNotAllowed,
				$"Generated interceptors namespace '{interceptorsNamespace}' is not listed in the InterceptorsNamespaces MSBuild property.",
				Location.None,
				IsError: false));
		}

		static IEnumerable<(string Key, object? Value)> GetAspectArguments(AttributeInfo attribute)
		{
			if (attribute.AspectAttributeData is {} aspectAttributeData)
				return GetAspectArguments(aspectAttributeData);

			if (attribute is { AspectAttributeSyntax: {} syntax, AspectSemanticModel: {} semanticModel })
				return GetAspectArguments(syntax, semanticModel);

			return [];
		}

		static void ValidateAspectHooks(
			List<DiagnosticInfo>                                                       diagnostics,
			Dictionary<ISymbol,(AttributeSyntax Syntax,SemanticModel SemanticModel)> aspectAttributes,
			HashSet<string>                                                           reportedDiagnostics)
		{
			foreach (var item in aspectAttributes)
			{
				var symbol          = item.Key;
				var aspectAttribute = item.Value;

				if (symbol is not INamedTypeSymbol aspectClass)
					continue;

				foreach (var arg in aspectAttribute.Syntax.ArgumentList?.Arguments ?? default)
				{
					if (arg.NameEquals is null || !IsHookName(arg.NameEquals.Name.Identifier.ValueText))
						continue;

					if (GetAttributeArgumentValue(arg.Expression, aspectAttribute.SemanticModel) is not string hookName || string.IsNullOrWhiteSpace(hookName))
						continue;

					var methods = aspectClass.GetMembers(hookName).OfType<IMethodSymbol>().ToArray();

					if (methods.Length == 0)
					{
						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							DiagnosticID.HookMethodNotFound,
							$"Aspect hook method '{hookName}' was not found on aspect type '{aspectClass.ToDisplayString()}'.",
							arg.Expression.GetLocation());
					}
					else if (!methods.Any(static m => m.IsStatic))
					{
						ReportDiagnostic(
							diagnostics,
							reportedDiagnostics,
							DiagnosticID.HookMethodNotStatic,
							$"Aspect hook method '{hookName}' on aspect type '{aspectClass.ToDisplayString()}' must be static.",
							arg.Expression.GetLocation());
					}
				}
			}
		}

		static void ReportDiagnostic(List<DiagnosticInfo> diagnostics, HashSet<string> reportedDiagnostics, string id, string message, Location? location)
		{
			var key = $"{id}:{location?.SourceTree?.FilePath}:{location?.SourceSpan.Start}:{message}";

			if (!reportedDiagnostics.Add(key))
				return;

			diagnostics.Add(new DiagnosticInfo(id, message, location));
		}

		static ImmutableArray<AspectFilterSet> BuildAssemblyFilters(
			Compilation                                                             compilation,
			List<DiagnosticInfo>                                                    diagnostics,
			Dictionary<ISymbol,(AttributeSyntax Syntax,SemanticModel SemanticModel)> aspectAttributes,
			HashSet<string>                                                          reportedDiagnostics)
		{
			var result = ImmutableArray.CreateBuilder<AspectFilterSet>();

			foreach (var filterAttribute in compilation.Assembly.GetAttributes())
			{
				if (!filterAttribute.NamedArguments.Any(static a => a.Key == "TargetFilter"))
					continue;

				if (CreateAppliedAspectFilterSet(
					diagnostics,
					aspectAttributes,
					reportedDiagnostics,
					filterAttribute) is {} filterSet)
					result.Add(filterSet);
			}

			return result.ToImmutable();
		}

		static ImmutableArray<AspectFilterSet> GetTypeFilters(
			Compilation                                                             compilation,
			List<DiagnosticInfo>                                                    diagnostics,
			Dictionary<ISymbol,(AttributeSyntax Syntax,SemanticModel SemanticModel)> aspectAttributes,
			Dictionary<INamedTypeSymbol,ImmutableArray<AspectFilterSet>>             typeFilterDic,
			INamedTypeSymbol?                                                       type,
			HashSet<string>                                                          reportedDiagnostics)
		{
			if (type is null)
				return [];

			if (typeFilterDic.TryGetValue(type, out var cached))
				return cached;

			var result = ImmutableArray.CreateBuilder<AspectFilterSet>();

			foreach (var syntaxReference in type.DeclaringSyntaxReferences)
			{
				if (syntaxReference.GetSyntax() is not TypeDeclarationSyntax typeDeclaration)
					continue;

				var semanticModel = compilation.GetSemanticModel(typeDeclaration.SyntaxTree);

				foreach (var filterAttribute in typeDeclaration.AttributeLists.SelectMany(static list => list.Attributes))
				{
					if (!HasNamedArgument(filterAttribute, "TargetFilter"))
						continue;

					if (CreateAppliedAspectFilterSet(
						diagnostics,
						aspectAttributes,
						reportedDiagnostics,
						filterAttribute,
						semanticModel) is {} filterSet)
						result.Add(filterSet);
				}
			}

			var filters = result.ToImmutable();
			typeFilterDic[type] = filters;

			return filters;
		}

		static AspectFilterSet? CreateAppliedAspectFilterSet(
			List<DiagnosticInfo>                                                    diagnostics,
			Dictionary<ISymbol,(AttributeSyntax Syntax,SemanticModel SemanticModel)> aspectAttributes,
			HashSet<string>                                                          reportedDiagnostics,
			AttributeSyntax                                                         filterAttribute,
			SemanticModel                                                            semanticModel)
		{
			if (semanticModel.GetSymbolInfo(filterAttribute).Symbol is not IMethodSymbol { ContainingType: var aspectClass })
				return null;

			var aspectAttributeData = aspectClass.GetAttributes().FirstOrDefault(aa => aa is { AttributeClass : { ContainingNamespace.Name : "AspectGenerator", Name : "AspectAttribute" }});

			AttributeInfo attributeInfo;

			if (aspectAttributes.TryGetValue(aspectClass, out var aspectAttribute))
				attributeInfo = new AttributeInfo(null, aspectClass, null, aspectAttribute.Syntax, aspectAttribute.SemanticModel);
			else if (aspectAttributeData is not null)
				attributeInfo = new AttributeInfo(null, aspectClass, aspectAttributeData, null, null);
			else
				return null;

			var filterValues = GetNamedStringFilterValue(filterAttribute, semanticModel, "TargetFilter");

			if (filterValues.IsDefaultOrEmpty)
				return null;

			var filters = CompileAspectFilters(diagnostics, reportedDiagnostics, filterValues, filterAttribute.GetLocation());

			return filters.IsDefaultOrEmpty ? null : new AspectFilterSet(attributeInfo, filters);
		}

		static AspectFilterSet? CreateAppliedAspectFilterSet(
			List<DiagnosticInfo>                                                    diagnostics,
			Dictionary<ISymbol,(AttributeSyntax Syntax,SemanticModel SemanticModel)> aspectAttributes,
			HashSet<string>                                                          reportedDiagnostics,
			AttributeData                                                           filterAttribute)
		{
			if (filterAttribute.AttributeClass is not {} aspectClass)
				return null;

			var aspectAttributeData = aspectClass.GetAttributes().FirstOrDefault(aa => aa is { AttributeClass : { ContainingNamespace.Name : "AspectGenerator", Name : "AspectAttribute" }});

			AttributeInfo attributeInfo;

			if (aspectAttributes.TryGetValue(aspectClass, out var aspectAttribute))
				attributeInfo = new AttributeInfo(null, aspectClass, null, aspectAttribute.Syntax, aspectAttribute.SemanticModel);
			else if (aspectAttributeData is not null)
				attributeInfo = new AttributeInfo(null, aspectClass, aspectAttributeData, null, null);
			else
				return null;

			var filterValues = GetNamedStringFilterValue(filterAttribute, "TargetFilter");

			if (filterValues.IsDefaultOrEmpty)
				return null;

			var location = filterAttribute.ApplicationSyntaxReference is {} syntaxReference
				? Location.Create(syntaxReference.SyntaxTree, syntaxReference.Span)
				: null;
			var filters = CompileAspectFilters(diagnostics, reportedDiagnostics, filterValues, location);

			return filters.IsDefaultOrEmpty ? null : new AspectFilterSet(attributeInfo, filters);
		}

		static ImmutableArray<string> GetNamedStringFilterValue(AttributeSyntax attribute, SemanticModel semanticModel, string name)
		{
			foreach (var arg in attribute.ArgumentList?.Arguments ?? default)
			{
				if (arg.NameEquals?.Name.Identifier.ValueText != name)
					continue;

				var value = GetAttributeArgumentValue(arg.Expression, semanticModel);

				return GetStringFilterValues(value);
			}

			return [];
		}

		static ImmutableArray<string> GetNamedStringFilterValue(AttributeData attribute, string name)
		{
			foreach (var arg in attribute.NamedArguments)
			{
				if (arg.Key != name)
					continue;

				return arg.Value.Kind == TypedConstantKind.Array
					? GetStringFilterValues(arg.Value.Values.Select(static v => v.Value).ToArray())
					: GetStringFilterValues(arg.Value.Value);
			}

			return [];
		}

		static ImmutableArray<string> GetStringFilterValues(object? value)
		{
			var result = ImmutableArray.CreateBuilder<string>();

			if (value is object?[] values)
			{
				foreach (var item in values)
					AddStringFilterValue(result, item as string);
			}
			else
				AddStringFilterValue(result, value as string);

			return result.ToImmutable();
		}

		static void AddStringFilterValue(ImmutableArray<string>.Builder result, string? value)
		{
			if (value is null)
				return;

			foreach (var line in value.Replace("\r\n", "\n").Replace('\r', '\n').Split('\n'))
			{
				var rule = line.Trim();

				if (rule.Length > 0 && !rule.StartsWith("#", StringComparison.Ordinal))
					result.Add(rule);
			}
		}

		static ImmutableArray<CompiledAspectFilter> CompileAspectFilters(
			List<DiagnosticInfo> diagnostics,
			HashSet<string>      reportedDiagnostics,
			ImmutableArray<string> filters,
			Location?            location)
		{
			var result = ImmutableArray.CreateBuilder<CompiledAspectFilter>();

			foreach (var filter in filters)
			{
				if (!TryParseAspectFilterRule(filter, out var isNegative, out var matcher, out var pattern))
					continue;

				if (matcher == FilterMatcher.Pattern)
					continue;

				if (matcher == FilterMatcher.Contains)
				{
					result.Add(
						new CompiledAspectFilter(
							isNegative,
							matcher,
							pattern,
							null));

					continue;
				}

				try
				{
					result.Add(
						new CompiledAspectFilter(
							isNegative,
							matcher,
							pattern,
							new Regex(pattern, RegexOptions.CultureInvariant, TimeSpan.FromMilliseconds(200))));
				}
				catch (ArgumentException ex)
				{
					ReportDiagnostic(
						diagnostics,
						reportedDiagnostics,
						DiagnosticID.InvalidAspectFilterRegex,
						$"Invalid aspect filter regex '{pattern}': {ex.Message}",
						location);
				}
			}

			return result.ToImmutable();
		}

		static bool TryParseAspectFilterRule(string filter, out bool isNegative, out FilterMatcher matcher, out string pattern)
		{
			isNegative = false;
			matcher    = FilterMatcher.Pattern;
			pattern    = "";

			var rule = filter.Trim();

			if (rule.Length == 0 || rule.StartsWith("#", StringComparison.Ordinal))
				return false;

			if (rule[0] == '-')
			{
				isNegative = true;
				rule       = rule[1..].TrimStart();
			}

			if (TryReadMatcherPrefix(rule, "pattern", out var patternBody))
			{
				matcher = FilterMatcher.Pattern;
				pattern = patternBody;
			}
			else if (TryReadMatcherPrefix(rule, "regex", out var regexBody))
			{
				matcher = FilterMatcher.Regex;
				pattern = regexBody;
			}
			else if (TryReadMatcherPrefix(rule, "contains", out var containsBody))
			{
				matcher = FilterMatcher.Contains;
				pattern = containsBody;
			}
			else
				pattern = rule;

			return pattern.Length > 0;
		}

		static bool TryReadMatcherPrefix(string rule, string prefix, out string body)
		{
			body = "";

			if (!rule.StartsWith(prefix, StringComparison.OrdinalIgnoreCase))
				return false;

			var index = prefix.Length;

			while (index < rule.Length && char.IsWhiteSpace(rule[index]))
				index++;

			if (index >= rule.Length || rule[index] != ':')
				return false;

			body = rule[(index + 1)..].Trim();
			return true;
		}

		static void AddMatchedFilterAttributes(List<AttributeInfo> attributes, ImmutableArray<AspectFilterSet> filterSets, string targetSignature)
		{
			foreach (var filterSet in filterSets)
			{
				if (!IsMatchedByFilters(filterSet.Filters, targetSignature))
					continue;

				if (attributes.Any(a => SymbolEqualityComparer.Default.Equals(a.AttributeClass, filterSet.Attribute.AttributeClass)))
					continue;

				attributes.Add(filterSet.Attribute);
			}
		}

		static bool IsMatchedByFilters(ImmutableArray<CompiledAspectFilter> filters, string targetSignature)
		{
			var matched = false;

			foreach (var filter in filters)
			{
				var isMatch = filter.Matcher switch
				{
					FilterMatcher.Contains => targetSignature.Contains(filter.Pattern, StringComparison.Ordinal),
					FilterMatcher.Regex    => IsRegexMatch(filter, targetSignature),
					_                      => false
				};

				if (!isMatch)
					continue;

				matched = !filter.IsNegative;
			}

			return matched;
		}

		static bool IsRegexMatch(CompiledAspectFilter filter, string targetSignature)
		{
			try
			{
				return filter.Regex?.IsMatch(targetSignature) == true;
			}
			catch (RegexMatchTimeoutException)
			{
				return false;
			}
		}

		static string GetCanonicalMethodFilterSignature(IMethodSymbol method)
		{
			var sourceMethod = method.ReducedFrom ?? method;
			var parts        = new List<string> { GetAccessibility(sourceMethod.DeclaredAccessibility) };

			if (sourceMethod.IsStatic)                              parts.Add("static");
			if (sourceMethod.IsAbstract)                            parts.Add("abstract");
			if (sourceMethod.IsVirtual && !sourceMethod.IsOverride) parts.Add("virtual");
			if (sourceMethod.IsOverride)                            parts.Add("override");
			if (sourceMethod.IsSealed)                              parts.Add("sealed");
			if (sourceMethod.IsExtern)                              parts.Add("extern");
			if (HasUnsafeModifier(sourceMethod))                    parts.Add("unsafe");

			var sb = new StringBuilder();

			sb
				.Append(string.Join(" ", parts))
				.Append(' ')
				.Append(FormatFilterType(sourceMethod.ReturnType))
				.Append(' ')
				.Append(FormatFilterType(sourceMethod.ContainingType))
				.Append('.')
				.Append(sourceMethod.Name);

			if (sourceMethod.TypeArguments.Length > 0)
				sb.Append('<').Append(string.Join(",", sourceMethod.TypeArguments.Select(FormatFilterType))).Append('>');

			sb.Append('(');

			var parameters = new List<string>();

			var methodParameters = sourceMethod.Parameters.AsEnumerable();

			if (method.ReducedFrom is not null || method.IsExtensionMethod || sourceMethod.IsExtensionMethod)
			{
				var receiverType = method.ReceiverType ?? sourceMethod.Parameters.FirstOrDefault()?.Type;

				if (receiverType is not null)
					parameters.Add($"this {FormatFilterType(receiverType)}");

				if (sourceMethod.Parameters.Length > 0)
					methodParameters = methodParameters.Skip(1);
			}

			foreach (var parameter in methodParameters)
			{
				var parameterText = new StringBuilder();

				if (parameter.IsParams)
					parameterText.Append("params ");

				parameterText.Append(parameter.RefKind switch
				{
					RefKind.Ref => "ref ",
					RefKind.Out => "out ",
					RefKind.In  => "in ",
					_           => ""
				});

				parameterText.Append(FormatFilterType(parameter.Type));
				parameters.Add(parameterText.ToString());
			}

			sb.Append(string.Join(",", parameters));
			sb.Append(')');

			return sb.ToString();
		}

		static bool HasUnsafeModifier(IMethodSymbol method)
		{
			foreach (var syntaxReference in method.DeclaringSyntaxReferences)
				if (syntaxReference.GetSyntax() is MethodDeclarationSyntax methodDeclaration &&
					methodDeclaration.Modifiers.Any(SyntaxKind.UnsafeKeyword))
					return true;

			return false;
		}

		static string GetAccessibility(Accessibility accessibility)
		{
			return accessibility switch
			{
				Accessibility.Public             => "public",
				Accessibility.Protected          => "protected",
				Accessibility.Internal           => "internal",
				Accessibility.Private            => "private",
				Accessibility.ProtectedOrInternal => "protected internal",
				Accessibility.ProtectedAndInternal => "private protected",
				_                                => "private"
			};
		}

		static string FormatFilterType(ITypeSymbol type)
		{
			if (type is IArrayTypeSymbol arrayType)
				return $"{FormatFilterType(arrayType.ElementType)}[]";

			if (type is ITypeParameterSymbol typeParameter)
				return typeParameter.Name;

			if (type is INamedTypeSymbol { IsGenericType: true } namedType)
			{
				var unbound = namedType.OriginalDefinition;
				var name    = unbound.Name;
				var tick    = name.IndexOf('`');

				if (tick >= 0)
					name = name[..tick];

				return $"{unbound.ContainingNamespace.ToDisplayString()}.{name}<{string.Join(",", namedType.TypeArguments.Select(FormatFilterType))}>";
			}

			return type.ToDisplayString(
				new SymbolDisplayFormat(
					typeQualificationStyle: SymbolDisplayTypeQualificationStyle.NameAndContainingTypesAndNamespaces,
					genericsOptions: SymbolDisplayGenericsOptions.IncludeTypeParameters,
					miscellaneousOptions: SymbolDisplayMiscellaneousOptions.EscapeKeywordIdentifiers));
		}

		static IEnumerable<(string Key, object? Value)> GetAspectArguments(AttributeData attribute)
		{
			return attribute.NamedArguments.Select(static arg => (
				arg.Key,
				arg.Value.Kind == TypedConstantKind.Array ? arg.Value.Values.Select(static v => v.Value).ToArray() : arg.Value.Value));
		}

		static IEnumerable<(string Key, object? Value)> GetAspectArguments(AttributeSyntax attribute, SemanticModel semanticModel)
		{
			foreach (var arg in attribute.ArgumentList?.Arguments ?? default)
				if (arg.NameEquals is not null)
					yield return (arg.NameEquals.Name.Identifier.ValueText, GetAttributeArgumentValue(arg.Expression, semanticModel));
		}

		static object? GetAttributeArgumentValue(ExpressionSyntax expression, SemanticModel semanticModel)
		{
			if (expression is ImplicitArrayCreationExpressionSyntax { Initializer.Expressions: var implicitValues })
				return implicitValues.Select(e => semanticModel.GetConstantValue(e).Value).ToArray();

			if (expression is ArrayCreationExpressionSyntax { Initializer.Expressions: var arrayValues })
				return arrayValues.Select(e => semanticModel.GetConstantValue(e).Value).ToArray();

			if (expression is CollectionExpressionSyntax { Elements: var elements })
				return elements
					.OfType<ExpressionElementSyntax>()
					.Select(e => semanticModel.GetConstantValue(e.Expression).Value)
					.ToArray();

			return semanticModel.GetConstantValue(expression).Value;
		}

		static bool IsAspectAttributeName(string name)

		{
			return name is
				"OnInit"            or
				"OnUsing"           or
				"OnUsingAsync"      or
				"OnBeforeCall"      or
				"OnBeforeCallAsync" or
				"OnCall"            or
				"OnAfterCall"       or
				"OnAfterCallAsync"  or
				"OnCatch"           or
				"OnCatchAsync"      or
				"OnFinally"         or
				"OnFinallyAsync";
		}

